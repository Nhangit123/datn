int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_FDCAN1_Init();
  MX_TIM2_Init();
  MX_TIM4_Init();
  MX_SPI2_Init();
  MX_TIM8_Init();
  MX_UART4_Init();
  /* USER CODE BEGIN 2 */
  MachineParameterConfig(&mPara); // Load hardware parameters such as L, rL, and C
  // LPF
  //  Initialize low-pass filters for measured signals

  // 1000 Hz: for fast voltage/current filtering
  vLPF_Init(&LPF_Vin, 1000, f_base_Ts);
  vLPF_Init(&LPF_Vout, 1000, f_base_Ts);
  vLPF_Init(&LPF_Vdclink, 1000, f_base_Ts);
  vLPF_Init(&LPF_I, 1000, f_base_Ts);
  vLPF_Init(&LPF_AD_I_Offset, 1000, f_base_Ts);

  // 2 Hz   : for slow reference/control smoothing
  vLPF_Init(&LPF_DI, 2, f_base_Tdk);

  // PID
  //  Initialize 3 PI controllers:
  //  Parameters: Ts, Kp, Ti, lambda, saturation limit
  vPI_Init(&PIVarV, f_base_Tdk, 0.0025, 0.004, 1, 50);   // - PIVarV  : outer voltage loop
  vPI_Init(&PIVarIb, f_base_Tdk, 0.0024, 0.004, 1, 200); // - PIVarIb : battery current loop
  vPI_Init(&PIVarIc, f_base_Tdk, 0.0034, 0.003, 1, 200); // - PIVarIc : capacitor current loop

  // FZ
  //  Initialize fuzzy membership sets and rules
  fuzzyInit();
  // //SMC
  // SMC_Init(&SMC_Ic, 0.5, 7);

  // ADC
  //  Initialize ADC conversion gains and offsets
  vAdc_DataInit(&adc_Objt_var);

  // start PWM
  //  Start PWM outputs for power stage control
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
  // Start PWM outputs for braking
  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_4);

  // Start periodic timer interrupt for the control loop
  HAL_TIM_Base_Start_IT(&htim2);

  //Init state machine
  StateMachine_Init();

  //PWM
  UART4_DMA_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	    if ((uart_tx_flag == 1U) && (uart4_tx_done_flag == 1U))
	    {
	        update_data_TX(&mCtr, &uartdata);

	        if (USER_UART_TxData(&uartdata) == HAL_OK)
	        {
	            uart_tx_flag = 0U;
	        }
	    }

	    if (uart4_rx_done_flag == 1U)
	    {
	        USER_UART_RxDataHandle();
	    }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

